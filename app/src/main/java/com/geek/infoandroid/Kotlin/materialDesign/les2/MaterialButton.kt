package com.geek.infoandroid.Kotlin.materialDesign.les2

class MaterialButton {
    //стиль кнопки
/*
   стили кнопок
        raised — кнопка по умолчанию (суффикс не требуется);
        unelevated — плоская кнопка без тени (Widget.MaterialComponents.Button.UnelevatedButton);
        outlined — очерченная кнопка (Widget.MaterialComponents.Button.OutlinedButton);
        text — текстовая кнопка (Widget.MaterialComponents.Button.TextButton).

        backgroundTint — изменение фона кнопки.
        iconTint — цвет иконки для кнопки (если она есть, и если она в векторе).
        rippleColor — цвет эффекта нажатия.
        strokeColor — цвет обводки кнопки. Цвет по умолчанию colorOnSurface для очерченной кнопки и transparent — для всех остальных.
        strokeWidth — толщина обводки.
        shapeAppearance — форма. По умолчанию shapeAppearanceSmallComponent.


 */


}